        private ReagentButton MakeReagentButton(string text, ReagentId id, bool isBuffer)
        {
            var btn = new ReagentButton(text, id, isBuffer);
            btn.OnPressed += args => OnReagentButtonPressed?.Invoke(args, btn, _transferAmount, IsOutputTab);
