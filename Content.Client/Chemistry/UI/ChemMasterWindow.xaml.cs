        private void BuildBufferInfo(ChemMasterBoundUserInterfaceState state)
        {
            BufferInfo.Children.Clear();

            if (!state.BufferReagents.Any())
            {
                BufferInfo.Children.Add(new Label { Text = Loc.GetString("chem-master-window-buffer-empty-text") });
                return;
            }

            var bufferHBox = new BoxContainer { Orientation = LayoutOrientation.Horizontal };
            BufferInfo.AddChild(bufferHBox);
            bufferHBox.AddChild(new Label { Text = $"{Loc.GetString("chem-master-window-buffer-label")} " });
            bufferHBox.AddChild(new Label { Text = $"{state.BufferCurrentVolume}u", StyleClasses = { StyleClass.LabelWeak } });

            IReadOnlyList<ReagentQuantity> bufferReagents = state.BufferReagents;
            if (_currentSortMethod == ReagentSortMethod.Amount)
                bufferReagents = bufferReagents.OrderByDescending(x => x.Quantity).ToList();
            else if (_currentSortMethod == ReagentSortMethod.Alphabetical)
                bufferReagents = bufferReagents.OrderBy(x =>
                {
                    _prototypeManager.TryIndex(x.Reagent.Prototype, out ReagentPrototype? proto);
                    return proto?.LocalizedName ?? string.Empty;
                }).ToList();

            var reagents = _currentSortMethod == ReagentSortMethod.Time ? state.BufferReagents : bufferReagents;
            int rowCount = 0;
            foreach (var (reagentId, quantity) in reagents)
            {
                _prototypeManager.TryIndex(reagentId.Prototype, out ReagentPrototype? proto);
                var name = proto?.LocalizedName ?? Loc.GetString("chem-master-window-unknown-reagent-text");
                if (name.Length > 17) name = AbbreviateReagentName(name);
                BufferInfo.Children.Add(BuildReagentRow(proto?.SubstanceColor ?? default, rowCount++, name, reagentId, quantity, true, true, state));
            }
        }

        private string AbbreviateReagentName(string fullName)
        {
            if (string.IsNullOrWhiteSpace(fullName)) return fullName;
            var words = fullName.Split(new[] { ' ', '\t', '\n', '\r' }, StringSplitOptions.RemoveEmptyEntries);
            if (words.Length == 0) return fullName;
            if (words.Length == 1)
            {
                var word = words[0];
                return word.Length > 5 ? word.Substring(0, word.Length - 5) : word;
            }
            var abbreviated = new List<string>();
            for (int i = 0; i < words.Length - 1; i++)
                abbreviated.Add(words[i].Length <= 3 ? words[i] : words[i].Substring(0, 3) + ".");
            abbreviated.Add(words[words.Length - 1]);
            return string.Join(" ", abbreviated);
        }

        private void BuildContainerUI(Control control, ContainerInfo? info, bool addReagentButtons)
        {
            control.Children.Clear();
            if (info is null)
            {
                control.Children.Add(new Label { Text = Loc.GetString("chem-master-window-no-container-loaded-text") });
                return;
            }

            control.Children.Add(new BoxContainer
            {
                Orientation = LayoutOrientation.Horizontal,
                Children =
                {
                    new Label { Text = $"{info.DisplayName}: " },
                    new Label { Text = $"{info.CurrentVolume}/{info.MaxVolume}", StyleClasses = { StyleClass.LabelWeak } }
                }
            });

            int rowCount = 0;
            if (info.Entities != null)
            {
                foreach (var (id, quantity) in info.Entities.Select(x => (x.Id, x.Quantity)))
                    control.Children.Add(BuildReagentRow(default, rowCount++, id, default, quantity, false, false));
            }
            if (info.Reagents != null)
            {
                foreach (var reagent in info.Reagents)
                {
                    _prototypeManager.TryIndex(reagent.Reagent.Prototype, out ReagentPrototype? proto);
                    var name = proto?.LocalizedName ?? Loc.GetString("chem-master-window-unknown-reagent-text");
                    if (name.Length > 17) name = AbbreviateReagentName(name);
                    control.Children.Add(BuildReagentRow(proto?.SubstanceColor ?? default, rowCount++, name, reagent.Reagent, reagent.Quantity, false, addReagentButtons));
                }
            }
        }

